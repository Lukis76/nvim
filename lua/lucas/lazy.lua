    { import = "lazyvim.plugins.extras.lang.json" },
    { import = "lazyvim.plugins.extras.ui.mini-animate" },
    -- import/override with your plugins
    { import = "plugins" },
  },
  checker = { enabled = true, notify = false },
  dev = {
    path = "~/lucas/plugins",
    fallback = true,
    patterns = { "lucas" },
  },
  performance = {
    rtp = {
      disabled_plugins = {
